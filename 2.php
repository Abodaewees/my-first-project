<?php
// math الرياضيات او الارقام 
$string = 20/10 ;
echo "<h1> $string</h1>" ; // هنا بس بكبر الخط باستخدام h1
echo "<br>";
echo abs(-30);// abs: بتطلع القيمة المطلقة (يعني بدون سالب) => الناتج 30
echo "<br>";
echo pow(5,2);// pow: بتعمل أس (5 أس 2 = 5*5 = 25)
echo "<br>";
echo sqrt(114); // sqrt: الجذر التربيعي (يعني العدد اللي نضربه في نفسه يطلع 114 تقريباً)
echo "<br>";
echo round(3.2); // round: بتقرب الرقم لأقرب عدد صحيح (3.2 تبقى 3)
echo "<br>";
echo floor(3.8);// floor: بتاخد الأرض، يعني تقرب لتحت (3.8 تبقى 3)
echo "<br>";
echo ceil(4.6); // ceil: بتاخد السقف، يعني تقرب لفوق (4.6 تبقى 5)
echo "<br>";
echo ceil(19.99);// نفس فكرة ceil: أي رقم فيه كسر هيبقى 20
echo "<br>";
echo 10 % 3 ; // % باقي القسمة (يعني 10 ÷ 3 = 3 والباقي 1)
// ++ ,-- ,+= ,-= ,/= ,*=
echo "<br>";
echo max(3,10); // max: أكبر رقم في القيم اللي بتدخلها (بيرجع 10)
echo "<br>";
echo min(3,10); // min: أصغر رقم في القيم اللي بتدخلها (بيرجع 3)












